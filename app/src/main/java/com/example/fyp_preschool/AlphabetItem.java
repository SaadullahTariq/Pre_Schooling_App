package com.example.fyp_preschool;

public class AlphabetItem {

    String word;
    int image;

    public AlphabetItem(String word, int image) {
        this.word = word;
        this.image = image;
    }

    public String getWord() {
        return word;
    }

    public int getImage() {
        return image;
    }
}
